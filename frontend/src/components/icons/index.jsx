export const Icon = ({ name, className = '', size = 24, color = 'currentColor' }) => {
  const IconComponent = iconMap[name]
  
  if (!IconComponent) {
    console.warn(`Icon "${name}" not found`)
    return null
  }
  
  return (
    <IconComponent 
      className={className} 
      size={size} 
      color={color} 
    />
  )
} 